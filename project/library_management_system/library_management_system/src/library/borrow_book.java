    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if(bid1.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ensure A Book is selected.");
        } else  if(rdate.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ensure Return Date is inputted.");
        } 
        else if(bid1.getText()!=null && rdate.getText()!=null){

            Connection connect = connectDB.connect();

            String book = bid1.getText();
            String date = rdate.getText();
            String stat = "Borrowed";

            try {

                    String sql = "UPDATE books SET Status = ?  WHERE Book_ID = ?";

                    PreparedStatement pst = connect.prepareStatement(sql);

                    pst.setString(1, stat);
                    pst.setString(2, book);

                    int rs = pst.executeUpdate();
                
                    String sql1 = "INSERT borrows(Book_ID, Return_Date) VALUES(?, ?)";

                    PreparedStatement pst1 = connect.prepareStatement(sql1);

                    pst1.setString(1, book);
                    pst1.setString(2, date);

                    int rs1 = pst1.executeUpdate();

                    if(rs1 == 1){

                        JOptionPane.showMessageDialog(rootPane, "Book has successfully been borrowed!");
                        this.dispose();

                        start_page lo = new start_page();
                        lo.setVisible(true);

                    }else {

                        JOptionPane.showMessageDialog(rootPane, "Book ID does not exist.");
                        bid1.setText("");
                        rdate.setText("");
                        bid.requestFocus();

                    }

                
            } catch (Exception e) {
                System.out.println(e);
            }

            bid1.setText("");
            rdate.setText("");

        }

    }//GEN-LAST:event_jButton3ActionPerformed

