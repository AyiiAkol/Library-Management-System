    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if(bid1.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ensure A Book is selected.");
        }     
        else if(bid1.getText()!=null){
            
        Connection connect = connectDB.connect();

        String uid = bid1.getText();
           
        try {
            
           String sql = "DELETE FROM borrows WHERE Book_ID = ?";
            
            PreparedStatement pst = connect.prepareStatement(sql);
            
            
           
            pst.setString(1, uid);
           
            
            int rs = pst.executeUpdate();
                
            String sql1 = "DELETE FROM books WHERE Book_ID = ?";
            
            PreparedStatement pst1 = connect.prepareStatement(sql1);
            
            
           
            pst1.setString(1, uid);
           
            
            int rs1 = pst1.executeUpdate();
            
            if(rs1 == 1){
            
                JOptionPane.showMessageDialog(rootPane, "Book has successfully been deleted!");
              this.dispose();
              
              start_page ho = new start_page();
              ho.setVisible(true); 
                   
              
                
            }else {
            
                JOptionPane.showMessageDialog(rootPane, "Book is not deleted.");
                 bid1.setText("");
                 bid1.requestFocus();
                        
            }
                
            
        } catch (Exception e) {
            System.out.println(e);
        }
        
        bid1.setText("");
            
        }  

    }//GEN-LAST:event_jButton3ActionPerformed

