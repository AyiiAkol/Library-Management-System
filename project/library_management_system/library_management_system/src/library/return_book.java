    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if(bid1.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ensure Borrow ID is selected.");
        } 
        else if(bid1.getText()!=null){

            Connection connect = connectDB.connect();

            String book = bid1.getText();
            String stat = "Returned";
            String fine = "";
            String stat1 = "Available";
            
            try{
                
            String sql2 = "SELECT * FROM borrows WHERE Borrow_ID = ?";

                    PreparedStatement pst2 = connect.prepareStatement(sql2);

                    pst2.setString(1, book);

                    ResultSet rs2 = pst2.executeQuery();

                    if(rs2.next()){
                        
                    String book1 = rs2.getString("Book_ID");
                    String date = rs2.getString("Return_Date");
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date date1 = null;
            try {
                date1 = sdf.parse(date);
            } catch (ParseException ex) {
                Logger.getLogger(return_book.class.getName()).log(Level.SEVERE, null, ex);
            }
            Date today1 = new Date();
            String today = today1.toString();
            
            if(today1.after(date1)){
                 fine = "500 kshs.";
            }else{
                 fine = "None.";
            }

            try {

                    String sql = "UPDATE books SET Status = ? WHERE Book_ID = ?";

                    PreparedStatement pst = connect.prepareStatement(sql);

                    pst.setString(2, book1);
                    pst.setString(1, stat1);                   

                    int rs = pst.executeUpdate();
                
                    String sql1 = "UPDATE borrows SET Status = ?, Actual_Return_Date = ?, Fine = ? WHERE Borrow_ID = ?";

                    PreparedStatement pst1 = connect.prepareStatement(sql1);

                    pst1.setString(4, book);
                    pst1.setString(2, today);
                    pst1.setString(3, fine);
                    pst1.setString(1, stat);

                    int rs1 = pst1.executeUpdate();

                    if(rs1 == 1){

                        JOptionPane.showMessageDialog(rootPane, "Book has successfully been returned!");
                        this.dispose();

                        start_page lo = new start_page();
                        lo.setVisible(true);

                    }else {

                        JOptionPane.showMessageDialog(rootPane, "Borrow ID does not exist.");
                        bid1.setText("");
                        bid.requestFocus();

                    }

                
            } catch (Exception e) {
                System.out.println(e);
            }

            bid1.setText("");
                    }
        }catch(Exception e){
           System.out.println(e);
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

