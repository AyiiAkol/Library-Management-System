    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if(bname.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ensure Book Name is selected.");
        } else  if(bdet.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ensure Book Details is inputted.");
        } else  if(pcom.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ensure Publishing Company is inputted.");
        }  else  if(pdate.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ensure Publish Date is inputted.");
        }
        else if(bname.getText()!=null && bdet.getText()!=null && pcom.getText()!=null && pdate.getText()!=null){

            Connection connect = connectDB.connect();

            String name = bname.getText();
            String det = bdet.getText();
            String com = pcom.getText();
            String date = pdate.getText();

            try {


                    String sql1 = "INSERT INTO books (Name, Details, Publishing_Company, Publish_Date) VALUES(?,?,?,?)";

                    PreparedStatement pst1 = connect.prepareStatement(sql1);

                    pst1.setString(1, name);
                    pst1.setString(2, det);
                    pst1.setString(3, com);
                    pst1.setString(4, date);

                    int rs1 = pst1.executeUpdate();

                    if(rs1 == 1){

                        JOptionPane.showMessageDialog(rootPane, "Book has successfully been added!");
                        this.dispose();

                        start_page lo = new start_page();
                        lo.setVisible(true);

                    }else {

                        JOptionPane.showMessageDialog(rootPane, "Book has not been added.");
                        bname.setText("");
                        bdet.setText("");
                        pcom.setText("");
                        pdate.setText("");
                        bname.requestFocus();

                    }

                
            } catch (Exception e) {
                System.out.println(e);
            }

            bname.setText("");
            bdet.setText("");
            pcom.setText("");
            pdate.setText("");

        }
    }//GEN-LAST:event_jButton3ActionPerformed

